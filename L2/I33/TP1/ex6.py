def recherche(L,S,p):
    tab = []
    sk = 0
    j = 0
    while j <= p:
        sk += L[j]
        j += 1
    if sk >= S:
        tab += [0]
    k = 1
    while (p+k) < len(L):

        sk = sk + L[p+k] - L[k-1]
        if sk >= S:
            tab += [k]
        k += 1
    return tab

