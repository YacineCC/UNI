def recherche(L,S,p):
    tab = []
    i = 0
    while i < len(L):
        som = 0
        k = i
        while k <= (p+i) and (p+i) < len(L):
            som += L[k]
            k += 1
        if som >= S:
            tab += [i]
        i += 1
    return tab

